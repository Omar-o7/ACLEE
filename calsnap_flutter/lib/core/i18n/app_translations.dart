import 'package:flutter/material.dart';
import 'package:shared_preferences/shared_preferences.dart';

/// Language provider — same pattern as the web app's I18nProvider.
/// Persists choice, exposes t(key) and text direction for RTL.
class LanguageProvider extends ChangeNotifier {
  String _lang = 'en';
  String get lang => _lang;
  bool get isArabic => _lang == 'ar';
  TextDirection get direction => isArabic ? TextDirection.rtl : TextDirection.ltr;

  LanguageProvider() {
    _load();
  }

  Future<void> _load() async {
    final prefs = await SharedPreferences.getInstance();
    _lang = prefs.getString('calsnap_lang') ?? 'en';
    notifyListeners();
  }

  Future<void> setLang(String l) async {
    _lang = l;
    notifyListeners();
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString('calsnap_lang', l);
  }

  String t(String key, [Map<String, Object>? vars]) {
    var s = _translations[_lang]?[key] ?? _translations['en']![key] ?? key;
    if (vars != null) {
      vars.forEach((k, v) => s = s.replaceAll('{$k}', v.toString()));
    }
    return s;
  }
}

